#include <stdio.h>

/**
 * main - prints the alphabet in lowercase, and then in uppercase
 * Return: Always 0
 */

int main(void)
{
	char n;
	char m;

	n = 'a';
	m = 'A';
	while
		(n <= 'z')
		{
			putchar(n);
			n++;
		}
	while
		(m <= 'Z')
		{
			putchar(m);
			m++;
		}
	putchar('\n');
		return (0);
}
