#include <stdio.h>

/**
 * main - print all the numbers of base 16 in lowercase
 * followed by a new line
 * Return: Always 0
 */

int main(void)
{
	char n;
	char m;

	n = 0;
	m = 'a';

	while
		(n <= '9');
	{
		putchar(n);
		n++;
	} while
		(m <= 'f');
	{
		putchar(m);
		m++;
	}
	putchar('\n');
	return (0);
}
